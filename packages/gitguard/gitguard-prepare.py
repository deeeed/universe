def detect_problematic_files(new_files: List[str]) -> List[Dict[str, Any]]:
    """Detect potentially problematic files among newly added files."""
    findings = []
    
    file_patterns = {
        "Environment Files": [
            r"\.env.*",
            r"config\.json",
            r"secrets\.yaml",
            r"credentials\.json",
        ],
        "Key Files": [
            r".*\.pem$",
            r".*\.key$",
            r".*\.keystore$",
            r".*\.p12$",
            r".*\.pfx$",
            r".*\.crt$",
            r".*\.cer$",
            r".*\.der$",
            r"id_rsa",
            r"id_dsa",
            r"id_ecdsa",
            r"id_ed25519",
        ],
        "Log Files": [
            r".*\.log$",
            r"npm-debug\.log.*",
            r"yarn-debug\.log.*",
            r"yarn-error\.log.*",
        ],
        "Database Files": [
            r".*\.sqlite$",
            r".*\.sqlite3$",
            r".*\.db$",
            r"dump\.sql$",
            r"database\.sql$",
        ],
        "Cache & Build Files": [
            r"\.DS_Store$",
            r"Thumbs\.db$",
            r"\.idea/",
            r"\.vscode/",
            r"node_modules/",
            r"\.next/",
            r"dist/",
            r"build/",
            r"coverage/",
        ],
    }
    
    for file in new_files:
        for category, patterns in file_patterns.items():
            for pattern in patterns:
                if re.search(pattern, file, re.IGNORECASE):
                    findings.append({
                        "type": category,
                        "file": file,
                        "pattern": pattern
                    })
                    break  # Stop after first match for this file
            
    return findings

